const LOCALES: Partial<Record<AppLanguage, Record<string, string>>> = {};

function buildLocale(lang: AppLanguage): Record<string, string> {
  if (lang === 'en') {
    return EN;
  }
  return { ...EN, ...LOCALES[lang] };
}

const cache: Partial<Record<AppLanguage, Record<string, string>>> = {};

function tableFor(lang: AppLanguage): Record<string, string> {
  if (!cache[lang]) {
    cache[lang] = buildLocale(lang);
  }
  return cache[lang]!;
}

export function t(key: string, lang: AppLanguage = getAppLanguage(), params?: Record<string, string | number>): string {
  let s = tableFor(lang)[key] ?? EN[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      s = s.split(`{${k}}`).join(String(v));
    }
  }
  return s;
}

/** Localized avoid chip label (UI). */
export function avoidLabel(id: AvoidPreference, lang: AppLanguage = getAppLanguage()): string {
  return t(`avoid.${id}`, lang);
}

/** Replace snake_case avoid tokens in AI lines with localized labels. */
export function humanizePreferenceMatchLine(line: string, lang: AppLanguage = getAppLanguage()): string {
  let out = line;
  for (const id of AVOID_PREFERENCE_IDS) {
    const label = avoidLabel(id as AvoidPreference, lang);
    out = out.replace(new RegExp(`\\b${escapeRegExp(id)}\\b`, 'g'), label);
  }
  return out;
}

